<script>
  import "@shoelace-style/shoelace/dist/components/button/button.js";
  import { goto } from "$app/navigation";
  import { onMount } from "svelte";
  import { State } from "$lib/engines/store.js";
  import { Delivery } from "$lib/engines/delivery/index.js";
  import { Feed } from "$lib/engines/delivery/index.js";

  export let delivery;

  let redraftButton, deleteButton;
  const Render = State.make();

  Render.cleanup = () => {
  };


  const Handle = {};
  
  Handle.redraft = () => {
    console.log( "redraft tbd" );
  }

  Handle.delete = async () => {
    if ( deleteButton.loading === true ) {
      return;
    }

    deleteButton.loading = true;
    try {
      await Delivery.unpublish( delivery );
      Feed.refresh();
    } catch ( error ) {
      console.error( error );
    }
    deleteButton.loading = false;
  }


  Render.reset();
  onMount(() => {
    return () => {
      Render.reset();
    };
  });
</script>


<div class="buttons">
  <!-- svelte-ignore a11y-click-events-have-key-events -->
  <sl-button
    bind:this={redraftButton}
    on:click={Handle.redraft}
    class="submit"
    size="medium"
    pill>
    Re-Draft
  </sl-button>

  <!-- svelte-ignore a11y-click-events-have-key-events -->
  <sl-button
    bind:this={deleteButton}
    on:click={Handle.delete}
    class="submit"
    size="medium"
    pill>
    Delete
  </sl-button>
</div>



